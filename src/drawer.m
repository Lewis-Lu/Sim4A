% --- Executes on mouse motion over figure - except title and menu.
function figure1_WindowButtonMotionFcn(hObject, eventdata, handles)
% hObject    handle to figure1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
axis([0 99 0 150]);
if handles.isPressed
    C = get(handles.axe, 'CurrentPoint');
    x = C(1,1);
    y = C(1,2);
    height = (10-handles.thickness)/50;
    handles.cache(end+1,:) = [x, y, height];
    fprintf("[EVENT]\t Mouse Button Motion in (%f, %f, %f)\n", x, y, height);
    
    i = 0:0.1:2*pi;
    x = x + handles.thickness*cos(i);
    y = y + handles.thickness*sin(i);
    vertices = [x' y'];
    f = polyshape(vertices);
    plot(f, 'FaceColor', handles.brushcolor, 'FaceAlpha', 0.2, 'LineStyle', 'none');
    
end
guidata(hObject, handles);


